-- ORACLE DDL FOR GENTLEMAN POS
-- Compatible con Oracle 12c o superior

-- 1. ROLES
CREATE TABLE ROLES (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NAME VARCHAR2(50) UNIQUE NOT NULL,
    DESCRIPTION VARCHAR2(255)
);

-- 2. USERS
CREATE TABLE USERS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    USERNAME VARCHAR2(50) UNIQUE NOT NULL,
    NAME VARCHAR2(100) NOT NULL,
    LAST_NAME VARCHAR2(100) NOT NULL,
    CEDULA VARCHAR2(20) UNIQUE,
    EMAIL VARCHAR2(255) UNIQUE NOT NULL,
    PASSWORD VARCHAR2(255) NOT NULL,
    IS_ACTIVE NUMBER(1) DEFAULT 1 NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL
);

-- 3. USER_ROLES (Relación N:N)
CREATE TABLE USER_ROLES (
    USER_ID NUMBER NOT NULL,
    ROLE_ID NUMBER NOT NULL,
    PRIMARY KEY (USER_ID, ROLE_ID),
    CONSTRAINT FK_UR_USER FOREIGN KEY (USER_ID) REFERENCES USERS(ID) ON DELETE CASCADE,
    CONSTRAINT FK_UR_ROLE FOREIGN KEY (ROLE_ID) REFERENCES ROLES(ID) ON DELETE CASCADE
);

-- 4. BLOCKED_USERS (Tracking de intentos fallidos)
CREATE TABLE BLOCKED_USERS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    USER_ID NUMBER UNIQUE NOT NULL,
    FAILED_ATTEMPTS NUMBER DEFAULT 0 NOT NULL,
    BLOCKED_UNTIL TIMESTAMP,
    LAST_FAILED_AT TIMESTAMP,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT FK_BU_USER FOREIGN KEY (USER_ID) REFERENCES USERS(ID) ON DELETE CASCADE
);

-- 5. CLIENTS
CREATE TABLE CLIENTS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    FIRST_NAME VARCHAR2(255),
    LAST_NAME VARCHAR2(255),
    EMAIL VARCHAR2(255) UNIQUE,
    PHONE VARCHAR2(50),
    ADDRESS VARCHAR2(255),
    IS_ACTIVE NUMBER(1) DEFAULT 1 NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    DELETED_AT TIMESTAMP
);

-- 6. PRODUCTS
CREATE TABLE PRODUCTS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NAME VARCHAR2(255) UNIQUE,
    PRICE NUMBER(12,2),
    STOCK NUMBER DEFAULT 0,
    VERSION NUMBER DEFAULT 0 NOT NULL,
    IS_ACTIVE NUMBER(1) DEFAULT 1 NOT NULL
);

-- 7. TAXES
CREATE TABLE TAXES (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NAME VARCHAR2(50) UNIQUE NOT NULL,
    RATE NUMBER(5,2) NOT NULL,
    IS_ACTIVE NUMBER(1) DEFAULT 1 NOT NULL
);

-- 8. PRODUCT_TAXES (Relación N:N)
CREATE TABLE PRODUCT_TAXES (
    PRODUCT_ID NUMBER NOT NULL,
    TAX_ID NUMBER NOT NULL,
    PRIMARY KEY (PRODUCT_ID, TAX_ID),
    CONSTRAINT FK_PT_PRODUCT FOREIGN KEY (PRODUCT_ID) REFERENCES PRODUCTS(ID) ON DELETE CASCADE,
    CONSTRAINT FK_PT_TAX FOREIGN KEY (TAX_ID) REFERENCES TAXES(ID) ON DELETE CASCADE
);

-- 9. INVOICES
CREATE TABLE INVOICES (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CLIENT_ID NUMBER,
    USER_ID NUMBER, -- Vendedor
    ISSUE_DATE TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    SUBTOTAL_SNAPSHOT NUMBER(12,2),
    TAX_TOTAL_SNAPSHOT NUMBER(12,2),
    TOTAL_SNAPSHOT NUMBER(12,2),
    TRANSACTION_ID VARCHAR2(255),
    STATUS VARCHAR2(20) DEFAULT 'CONFIRMED' NOT NULL,
    PAYMENT_METHOD VARCHAR2(20) DEFAULT 'CASH' NOT NULL,
    IS_ACTIVE NUMBER(1) DEFAULT 1 NOT NULL,
    VERSION NUMBER DEFAULT 0 NOT NULL,
    CONSTRAINT FK_INV_CLIENT FOREIGN KEY (CLIENT_ID) REFERENCES CLIENTS(ID),
    CONSTRAINT FK_INV_USER FOREIGN KEY (USER_ID) REFERENCES USERS(ID)
);

-- 10. INVOICE_DETAILS
CREATE TABLE INVOICE_DETAILS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    INVOICE_ID NUMBER NOT NULL,
    PRODUCT_ID NUMBER,
    PRODUCT_NAME VARCHAR2(255), -- Snapshot del nombre
    QUANTITY NUMBER NOT NULL,
    UNIT_PRICE_SNAPSHOT NUMBER(12,2) NOT NULL,
    CONSTRAINT FK_DET_INV FOREIGN KEY (INVOICE_ID) REFERENCES INVOICES(ID) ON DELETE CASCADE,
    CONSTRAINT FK_DET_PROD FOREIGN KEY (PRODUCT_ID) REFERENCES PRODUCTS(ID)
);

-- 11. INVOICE_DETAIL_TAXES
CREATE TABLE INVOICE_DETAIL_TAXES (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    DETAIL_ID NUMBER NOT NULL,
    TAX_ID NUMBER,
    TAX_NAME VARCHAR2(50), -- Snapshot
    RATE_SNAPSHOT NUMBER(5,2) NOT NULL,
    AMOUNT_SNAPSHOT NUMBER(12,2) NOT NULL,
    CONSTRAINT FK_IDT_DET FOREIGN KEY (DETAIL_ID) REFERENCES INVOICE_DETAILS(ID) ON DELETE CASCADE,
    CONSTRAINT FK_IDT_TAX FOREIGN KEY (TAX_ID) REFERENCES TAXES(ID)
);

-- 12. STOCK_MOVEMENTS
CREATE TABLE STOCK_MOVEMENTS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    PRODUCT_ID NUMBER NOT NULL,
    QUANTITY NUMBER NOT NULL,
    TYPE VARCHAR2(20) NOT NULL, -- 'SALE', 'ADJUSTMENT', 'PURCHASE'
    REASON VARCHAR2(255),
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT FK_SM_PROD FOREIGN KEY (PRODUCT_ID) REFERENCES PRODUCTS(ID) ON DELETE CASCADE
);

-- 13. ERROR_LOGS
CREATE TABLE ERROR_LOGS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    MESSAGE CLOB NOT NULL,
    STACK CLOB,
    CONTEXT VARCHAR2(255),
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL
);

-- INDICES PARA PERFORMANCE
CREATE INDEX IDX_USERS_EMAIL ON USERS(EMAIL);
CREATE INDEX IDX_USERS_USERNAME ON USERS(USERNAME);
CREATE INDEX IDX_PRODUCTS_NAME ON PRODUCTS(NAME);
CREATE INDEX IDX_INVOICES_DATE ON INVOICES(ISSUE_DATE);
CREATE INDEX IDX_CLIENTS_EMAIL ON CLIENTS(EMAIL);

